    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;经常能看到报道，某某8W入场做到1000W。这样的消息确实很让人振奋，然而也不能被巨大的收益冲昏头脑，<strong>不是所有人都适合炒单。</strong></p>
