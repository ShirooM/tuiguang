    <p>2、期货是怎么利用杠杆来以小博大的？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;举个例子就明白了，你觉得棉花要涨价，想要买入1万块的棉花合约，在期货交易里，你不用真的出1万块，你只需要付给期货公司10%的保证金，就能买入1W块的棉花合约，也就是1千块。过了一段时间棉花涨价了，1万块的棉花，可以卖12000块。这个时候你卖出棉花合约。你赚到2000块，本金是1000块，净赚1000。<strong>这就是期货的保证金交易制度，就是为了方面投资者在没有那么多本金的情况下，也可以抓住机遇，以小博大。</strong></p>
    <p>3、炒期货需要多少钱？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;期货根据你投资的品种不同，所需要的资金也不同，股指期货最贵，商品期货相对便宜的多。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一般情况下，个人在交易所开户，入门门槛基本都是50W起，门槛太高。建议大家在二级券商，或者代理券商开户。这些地方有配资，大大降低了入门门槛。例如交易所棉花一手的保证金最少4000块，经过代理配资，保证金在500块左右就能玩一手，<strong>但是代理存在很严重的手续费抽成问题，</strong>正常情况下，棉花的手续费最多5块一手，代理的手续费在60-100不等。</p>
    <p>二、期货的种类有哪些？_哪些种类适合新手？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>期货可以大致分为两大类，商品期货与金融期货。</strong>商品期货分为农产品期货、金属期货（基础金属与贵金属，比如螺纹钢期货和黄金期货）、能源期货（最有代表的就是原油期货）；金融期货中主要品种可以分为外汇期货、利率期货（包括中长期债券期货和短期利率期货）和股指期货。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>我接触的所有过来咨询的投资者，80%以上都想要做黄金，</strong>确实由于黄金市场波动大，更适合投机。但是短线的操作技巧很难把握，<strong>对于新手来说，商品期货价格低，波动小，保证金和手续费低，更适合。</strong>推荐新手从焦煤、焦炭、螺纹钢、玉米，豆粕、菜粕这几大商品期货入手。</p>
    <p>三、怎么选择开户公司？保证资金安全是成功的第一步</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;期货开户需要找期货经纪公司，<strong>开户都是免费的。</strong>正规期货公司开的期货账户都是可以在<strong>中国保证金监控中心查询</strong>到的，<strong>并且正规的期货公司，账户金额都是有第三方监管的。</strong></p>
