    <p>一、看图片一分钟了解什么是期货？_炒期货需要多少钱？</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;期货和股票一样，买卖的都不是实物。股票买卖的是某公司的股份，期货买卖的是某商品的合约。</p>


    <p>1、期货可以双向交易，在市场行情不好的情况下一样可以赚钱。</p>
    <p style="text-align: center;">
        <img src="./upload/33.png" onclick="viewImg(this)" width="80%" />
