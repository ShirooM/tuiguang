    <p>一、影响黄金期货波动的3个市场因素：</p>
    <p>1、美元汇率走势</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;由于黄金市场价格是以美元标价的，一般情况下，美元升值会促使黄金价格下跌，而美元贬值又会推动黄金价格上涨，两者呈负相关。不过，在某些特殊时段，尤其是黄金走势非常强或非常弱的时期，黄金价格很有可能摆脱美元影响，走出独立行情。</p>
    <p>2、国际石油价格</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前，世界主要石油现货与期货市场的价格都以美元标价，石油价格的涨跌不仅反映了世界石油供求关系，而且也在一定程度上反映了美元汇率的变化。一般情况下，石油价格与黄金价格是间接互相影响的，国际金价与原油期货价格的涨跌通常存在正相关关系。</p>
    <p>3、国际政治经济局势</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;通常而言，国际上发生的重大政治、战争事件都会影响黄金价格波动。当战争爆发、政局不稳定或经济萧条时，黄金的避险保值功能得到突显，大量投资者转向黄金保值投资，从而扩大对黄金的需求，刺激金价上扬。</p>
    <p><strong>以上3个影响黄金的因素，一定要保持关注，消息发布的当天，盘面必然有大的变动！</strong></p>
