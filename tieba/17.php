    <p>三、黄金期货实战看盘3技巧_发现主力意图</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在交易的过程中，货投资者可通过看盘，发现主力的一举一动，以免被引诱做出错误的买卖决定。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1、出现大单：当买卖队列中出现大量买卖盘，且成交大单不断时，往往代表主力资金活跃。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2、出现扫盘：扫盘，即在涨势中常有大单从天而降，将卖盘挂单全部吞掉，在期价刚刚形成多头排列，且涨势初起之际，若发现有大单迅速横扫了买卖队列中的多笔卖盘，便可以认为主力正大举进场建仓，投资者可以抓住机会跟进。</p>
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;3、从上压板、下托板看主力意图和期价方向：上压板是指大量的卖盘挂单，而下托板即大量的买盘挂单。不管是哪一种，其目的都是为了操纵期价，诱人跟风，且当商品处于不同价区时，它们的作用是不同的。例如，如果下托板是在期价升幅大且处于高价区时出现，而走势却是价滞量增，则可以认为是主力在诱多出货。但如果出现的是较多的上压板，且上涨无量时，即表示顶部即将出现，期价将要下跌。</p>

